public class Punto {

    //stato interno
    private int x, y;

    //METODI

    //Costruttori
    public Punto () {

        this.x = x;
        this.y = y;
    }

    // setter di x
    public void setX(int x) {
        this.x = x;
    }

    //getter di x
    public int getX() {
        return x;
    }

    // setter di y
    public int getY() {
        return y;
    }

    //getter di y
    public void setY(int y) {
        this.y = y;
    }
}
